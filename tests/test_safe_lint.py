def test_blocked_patterns_stay_blocked():
    for s in BLOCKED:
        assert safe_lint.lint(s), f"차단되어야 함: {s}"


def test_direct_tone_allowed():
    for s in ALLOWED:
        v = safe_lint.lint(s)
        assert not v, f"허용되어야 함: {s} -> {v}"


def test_outcome_combo_only_not_bare_adverb():
    # 같은 부사라도 결과 동사와 결합할 때만 차단
    assert safe_lint.lint("반드시 이루어집니다.")
    assert not safe_lint.lint("반드시 확인해 보세요.")
