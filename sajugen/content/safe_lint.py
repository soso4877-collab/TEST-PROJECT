# -*- coding: utf-8 -*-
"""§12 안전표현 린터 — 단정·공포·보장 표현 차단(보고서 §12 치환표 기반).

명리/자미두수는 전통 해석 체계. 의료·법률·투자·생사·합격·재회 단정 금지.
"""

from __future__ import annotations

import re

# (패턴, 사유, 권장 대체)
RULES: list[tuple[str, str, str]] = [
    (r"반드시|무조건|틀림없(이|다)|확실히\s*(된다|됩니다)", "단정", "~하는 경향이 있습니다"),
    (
        r"100\s*%|100퍼|백퍼|완벽\s*적중|반드시\s*적중",
        "과장·허위",
        "전통 해석상 이렇게 봅니다(참고용)",
    ),
    (r"큰일\s*(난다|납니다)|망(한다|합니다)|끝장|쫄딱", "공포", "이 시기에는 신중함이 필요합니다"),
    (
        r"(반드시|무조건)?\s*재회(한다|합니다|함|보장)|꼭\s*돌아온다",
        "관계 단정",
        "관계에서 점검해볼 부분이 있습니다",
    ),
    (
        r"헤어진다|이혼(한다|합니다|함)|파혼(한다|확정)",
        "관계 단정",
        "관계 흐름을 함께 살펴볼 수 있습니다",
    ),
    (
        r"돈을?\s*잃는다|쪽박|대박|떼돈|반드시\s*부자|일확천금\s*(보장|확정)",
        "재물 단정",
        "재정 결정 전 확인할 포인트가 있습니다",
    ),
    (
        r"병(이|에)\s*(생긴다|걸린다)|암\s*(걸|발병)|불치|수명(이|은)?\s*짧",
        "의료·생사 단정",
        "생활에서 살펴보면 좋은 부분입니다",
    ),
    (
        r"합격(한다|확정|보장)|불합격(한다|확정)|임신(한다|확정)|득남|득녀\s*확정",
        "결과 단정",
        "준비 과정에서 강점을 이렇게 활용할 수 있습니다",
    ),
    (
        r"운명(이|은)\s*(정해|결정).?(졌|되어 ?있)",
        "운명론",
        "타고난 경향이며 선택과 노력으로 달라집니다",
    ),
    (r"죽(는다|습니다)|사망(한다|시기)|단명", "생사 단정", "건강을 살피는 시기로 볼 수 있습니다"),
]
_COMPILED = [(re.compile(p), why, alt) for p, why, alt in RULES]


def lint(text: str) -> list[dict]:
    """위반 목록. 빈 리스트면 통과."""
    out = []
    for rx, why, alt in _COMPILED:
        for m in rx.finditer(text):
            out.append({"match": m.group(0), "why": why, "suggest": alt, "pos": m.start()})
    return out


def is_clean(text: str) -> bool:
    return not lint(text)
