# -*- coding: utf-8 -*-
"""§12 안전표현 린터 — '결과 보장·적중·의료/생사·운명론'만 차단.

2026-06-12 운영자 지시로 완화: 단정 부사('반드시' 등) 단독은 허용하고,
부사+결과 동사 결합(보장 진술)만 차단한다. 직설 상담 톤(계산 사실의 단정,
운 흐름의 직설 서술)은 허용 — 미래 결과의 보장·확정만 막는다.
표시광고법·플랫폼 정책 리스크 패턴(적중·100%·의료·생사·운명론)은 불변.
"""

from __future__ import annotations

import re

# 결과 동사(이게 따라붙을 때만 단정 부사를 차단) — 행동 권유("반드시 짚고
# 넘어가야 합니다")는 허용되도록 일반 동사(한다/합니다)는 넣지 않는다.
_OUTCOME = (
    "된다|됩니다|이루어|성사|풀린다|풀립니다|결혼하|결혼합|합격하|합격합|"
    "임신하|임신합|돌아온다|돌아옵니다|이혼하|헤어지"
)

# (패턴, 사유, 권장 대체)
RULES: list[tuple[str, str, str]] = [
    (
        r"100\s*%|100퍼|백퍼|완벽\s*적중|반드시\s*적중|정확히\s*맞(춘|힌)",
        "과장·허위(적중 주장)",
        "전통 해석상 이렇게 봅니다",
    ),
    (
        rf"(반드시|무조건|틀림없이|확실히|꼭)[^.\n!?]{{0,14}}({_OUTCOME})",
        "결과 보장",
        "~할 가능성이 큽니다 / ~하기 좋은 흐름입니다",
    ),
    (r"큰일\s*(난다|납니다)|망(한다|합니다)|끝장|쫄딱", "공포", "이 시기에는 신중함이 필요합니다"),
    (
        r"재회(한다|합니다|하게\s*됩니다|보장|확정)|꼭\s*돌아온다"
        r"|(반드시|결국|무조건|곧)\s*(헤어|이혼|파혼)|파혼\s*확정",
        "관계 결과 보장",
        "관계의 흐름과 확인할 결로 짚어 드립니다",
    ),
    (
        r"(반드시|크게|무조건)\s*돈을?\s*잃|쪽박|대박|떼돈|일확천금|(반드시|무조건)\s*부자",
        "재물 보장·과장",
        "재물의 결과 시기의 흐름으로 짚어 드립니다",
    ),
    (
        r"병(이|에)\s*(생긴|걸린|걸립)|암(에)?\s*(걸|발병)|불치|수명(이|은)?\s*짧",
        "의료·생사 단정",
        "생활에서 살펴보면 좋은 부분입니다",
    ),
    (r"죽(는다|습니다)|사망(한다|시기)|단명", "생사 단정", "건강을 살피는 시기로 볼 수 있습니다"),
    (
        r"합격\s*(한다|합니다|확정|보장)|불합격\s*(한다|확정)|임신\s*(한다|합니다|확정)|득남|득녀\s*확정",
        "결과 단정",
        "준비가 결실로 이어지기 좋은 흐름입니다",
    ),
    (
        r"운명(이|은)?\s*(정해(졌|져|진)|결정(되|돼))",
        "운명론",
        "타고난 경향이며 선택과 노력으로 달라집니다",
    ),
]
_COMPILED = [(re.compile(p), why, alt) for p, why, alt in RULES]


def lint(text: str) -> list[dict]:
    """위반 목록. 빈 리스트면 통과."""
    out = []
    for rx, why, alt in _COMPILED:
        for m in rx.finditer(text):
            out.append({"match": m.group(0), "why": why, "suggest": alt, "pos": m.start()})
    return out


def is_clean(text: str) -> bool:
    return not lint(text)
