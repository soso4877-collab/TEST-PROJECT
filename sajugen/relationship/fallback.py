def stabilize_section_lengths(sections: list[object]) -> None:
    for section in sections:
        filler = TAIL_FILLERS.get(section.id)
        if not filler:
            continue
        text = section.final_text.strip()
        if 250 <= len(text) <= 1800 and filler not in text:
            section.final_text = (text + "\n\n" + filler).strip()
